package com.shab.artificon.utils;

/**
 * @author zentere
 *
 */
public class ModelResponse {

	private String name;

	public ModelResponse(String name) {
		super();
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
